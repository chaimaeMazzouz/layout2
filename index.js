function goOnline() {
  var x = document.getElementById("online");
  var y = document.getElementById("offline");
  x.style.display = "block";
  y.style.display = "none";
}
function goOffline() {
  var x = document.getElementById("online");
  var y = document.getElementById("offline");
  y.style.display = "block";
  x.style.display = "none";
}
